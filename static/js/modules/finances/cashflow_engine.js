const CashflowEngine = {
    getFilteredFinanceData: function() {
        // BEZBEDNO DEKONSTRUISANJE (Sprečava pucanje ako su filteri prazni)
        const filters = state.activeFilters.finances || {};
        const range = filters.range || 'thisMonth';
        const startDate = filters.startDate;
        const endDate = filters.endDate;
        
        let start, end; 
        const now = new Date();
        
        if (range === 'custom' && startDate && endDate) { 
            start = new Date(startDate); start.setHours(0,0,0,0); 
            end = new Date(endDate); end.setHours(23,59,59,999); 
        } else {
            end = new Date(); end.setHours(23,59,59,999); 
            start = new Date(); start.setHours(0,0,0,0);
            switch(range) { 
                case 'today': break; 
                case 'thisWeek': start.setDate(start.getDate() - start.getDay() + (start.getDay() === 0 ? -6 : 1)); break; 
                case 'thisMonth': start = new Date(now.getFullYear(), now.getMonth(), 1); break; 
                case 'thisYear': start = new Date(now.getFullYear(), 0, 1); break; 
                default: start = new Date(now.getFullYear(), now.getMonth(), 1); 
            }
        }
        return (state.data.deals || []).filter(deal => { 
            const dealDate = new Date(deal.buyerPaidOn || deal.lastModified); 
            return dealDate >= start && dealDate <= end; 
        });
    },

    applyRecurringExpenses: async function() {
        if (!state.data.recurringExpenses) return;
        const today = new Date();
        today.setHours(23, 59, 59, 999); 
        
        for (const expense of state.data.recurringExpenses) {
            if(!expense.startDate) continue; 
            
            let processDate = expense.lastApplied ? new Date(expense.lastApplied) : new Date(expense.startDate);
            let currentMonthCheck = new Date(processDate.getFullYear(), processDate.getMonth(), 1);
            
            while (true) {
                const year = currentMonthCheck.getFullYear();
                const month = currentMonthCheck.getMonth();
                
                const execDay = Math.min(expense.dayOfMonth, new Date(year, month + 1, 0).getDate());
                const execDate = new Date(year, month, execDay, 12, 0, 0); 
                
                if (execDate > today) break; 
                if (expense.endDate && execDate > new Date(expense.endDate)) break; 
                
                const lastApp = expense.lastApplied ? new Date(expense.lastApplied) : null;
                const startD = new Date(expense.startDate);
                startD.setHours(0,0,0,0);
                
                const isAfterLastApplied = lastApp ? execDate > lastApp : execDate >= startD;
                
                if (isAfterLastApplied) {
                    const transId = Utils.generateId();
                    const nowIso = new Date().toISOString();
                    const newTx = {
                        id: transId,
                        type: 'expense',
                        date: execDate.toISOString().slice(0, 10),
                        time: "12:00", 
                        accountId: expense.accountId,
                        description: expense.description + ` (${Utils.t('cashflow.autoGenerated')})`,
                        amount: expense.amount,
                        currency: expense.currency,
                        category: expense.category,
                        status: 'completed',
                        reference: `AUTO-${transId.substring(0,5).toUpperCase()}`,
                        createdAt: nowIso,
                        lastModified: nowIso,
                        isAutoGenerated: true
                    };
                    state.data.transactions.push(newTx);
                    try {
                        await saveSingleItem('transactions', newTx);
                        expense.lastApplied = execDate.toISOString();
                        await saveSingleItem('recurringExpenses', expense);
                    } catch(err) {
                        console.error("Failed to save recurring expense:", err);
                    }
                }
                currentMonthCheck.setMonth(currentMonthCheck.getMonth() + 1);
            }
        }
    }
};